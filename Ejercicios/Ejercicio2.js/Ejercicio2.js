'use strict'; 
let numero = parseFloat(prompt('Introduzca el numero a evaluar'));
/**
 * Estructuras de control
 * if else, switch, for, while, do while
 */
if (numero>0){
    alert(`El número: ${numero} es positivo`); 
}else{
    if (numero <0)
        alert(`El número: ${numero} es negativo`);
    else alert(`El número: ${numero} es cero`);
}


